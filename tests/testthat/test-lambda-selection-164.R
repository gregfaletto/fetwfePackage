# ------------------------------------------------------------------------------
# Test 10: CV coverage validation — a SMALL fast DGP with 10 reps.
#
# This is a smoke test, NOT a re-validation of Phase B. The SPEC's
# acceptance criterion #5: "a small fixed-θ test asserts that
# `lambda_selection = "cv"` on a single representative DGP gives
# `mean(att_hat - true_att)` bias `< 0.10` in absolute value. This is
# a coverage-direction check, not a stat-test — the SPEC's empirical
# validation already covered the statistical claim."
#
# DGP: paper's first-simulation downscope (R=3, T=5, d=2, density=0.5,
# eff_size=2). N=200, 10 reps. Runs in seconds.
# ------------------------------------------------------------------------------

test_that("CV path has |bias| < 0.10 on a representative DGP (smoke test)", {
	skip_on_cran()

	template <- genCoefs(
		G = 3,
		T = 5,
		d = 2,
		density = 0.5,
		eff_size = 2,
		seed = 7777L
	)
	te_truth <- getTes(template)

	n_reps <- 10L
	biases <- numeric(n_reps)
	# As of #250 the panel RNG is controlled by the `seed` argument to
	# simulateData() (it no longer reuses coefs$seed); pass a distinct
	# `seed = 13000L + i` per rep to vary the noise across replications.
	for (i in seq_len(n_reps)) {
		sim <- simulateData(
			template,
			N = 200,
			sig_eps_sq = 1,
			sig_eps_c_sq = 0.5,
			seed = 13000L + i
		)
		# True ATT = sum(actual_cohort_tes * pi_cond) where pi_cond is
		# the in-sample treated proportions (excluding never-treated).
		pi_cond <- sim$indep_counts[-1] / sum(sim$indep_counts[-1])
		true_att <- sum(te_truth$actual_cohort_tes * pi_cond)
		res <- fetwfeWithSimulatedData(sim)
		biases[i] <- res$att_hat - true_att
	}
	mean_bias <- mean(biases)
	# Coverage-direction check: |bias| should be small, not statistically
	# negligible. Phase B's main DGP at N=200 gave CV-bridge bias = -0.069
	# at 100 reps; we expect similar magnitude at 10 reps on this DGP.
	# Tolerance 0.10 is loose enough to absorb 10-rep Monte Carlo error.
	expect_lt(abs(mean_bias), 0.10)
})

# ------------------------------------------------------------------------------
# Test: getBetaCV() preserves caller's .Random.seed (regression for #177).
#
# Before the fix, `set.seed(cv_seed)` inside getBetaCV() leaked into the
# caller's .GlobalEnv$.Random.seed, so a second set.seed(K) + runif() after
# the fit would diverge from a baseline set.seed(K) + runif() without the
# fit. The BIC path never mutated RNG (no set.seed call); we lock BOTH paths
# now so a future regression on either is caught.
# ------------------------------------------------------------------------------

test_that("CV path preserves caller's .Random.seed (#177)", {
	sim <- .lambda_sel_fixture()
	set.seed(42L)
	before <- runif(3L)
	set.seed(42L)
	invisible(fetwfeWithSimulatedData(sim))
	after <- runif(3L)
	expect_identical(before, after)
})

test_that("BIC path preserves caller's .Random.seed (#177)", {
	sim <- .lambda_sel_fixture()
	set.seed(42L)
	before <- runif(3L)
	set.seed(42L)
	invisible(fetwfeWithSimulatedData(sim, lambda_selection = "bic"))
	after <- runif(3L)
	expect_identical(before, after)
})

# ------------------------------------------------------------------------------
# (#185 G1): the default CV path composes with se_type = "cluster" and with
# add_ridge = TRUE -- cross-paths the other CV tests above leave unexercised.
# Both work empirically; this locks the cross-path behavior so a regression on
# either the cluster sandwich or the ridge augmentation under CV is caught.
# ------------------------------------------------------------------------------
test_that("CV path composes with se_type = 'cluster' and with add_ridge", {
	sim <- .lambda_sel_fixture()

	res_cl <- fetwfeWithSimulatedData(sim, se_type = "cluster")
	expect_identical(res_cl$lambda_selection, "cv")
	expect_identical(res_cl$se_type, "cluster")
	expect_true(is.finite(res_cl$att_hat))
	expect_true(is.finite(res_cl$att_se))
	expect_gt(res_cl$att_se, 0)

	res_ridge <- fetwfeWithSimulatedData(sim, add_ridge = TRUE)
	expect_identical(res_ridge$lambda_selection, "cv")
	expect_true(is.finite(res_ridge$att_hat))
	expect_true(is.finite(res_ridge$att_se))
})

# ------------------------------------------------------------------------------
# (#185 IC1): lambda.max / lambda.min / nlambda are silently ignored under the
# default lambda_selection = "cv" (cross-validation builds its own grid). Warn
# so the user isn't surprised; stay silent on the BIC path and on cv with the
# default grid.
# ------------------------------------------------------------------------------
test_that("cv path warns when lambda.max / lambda.min / nlambda are supplied", {
	sim <- .lambda_sel_fixture()
	expect_warning(
		fetwfeWithSimulatedData(sim, lambda.max = 5),
		"ignored under lambda_selection = 'cv'",
		fixed = TRUE
	)
	expect_warning(
		fetwfeWithSimulatedData(sim, nlambda = 50L),
		"ignored under lambda_selection = 'cv'",
		fixed = TRUE
	)
	# betwfe() shares the same dispatch.
	expect_warning(
		betwfeWithSimulatedData(sim, lambda.min = 0.01),
		"ignored under lambda_selection = 'cv'",
		fixed = TRUE
	)
	# BIC path uses the supplied grid -> no IC1 warning.
	expect_no_warning(
		fetwfeWithSimulatedData(sim, lambda_selection = "bic", lambda.max = 5)
	)
	# CV with the default grid -> no warning.
	expect_no_warning(fetwfeWithSimulatedData(sim))
})
