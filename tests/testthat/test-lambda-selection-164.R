# Tests for issue #164 — `lambda_selection` parameter on `fetwfe()` /
# `betwfe()` (v1.13.0): adds 10-fold CV alongside the BIC selection
# path, changes the default from BIC to CV.
#
# Coverage:
#   1. Default `lambda_selection` is `"cv"`.
#   2. CV path produces finite, sensible results.
#   3. CV path is deterministic for the same `cv_seed`.
#   4. CV path differs for different `cv_seed` values.
#   5. BIC path produces byte-identical results to a small fixed-θ DGP
#      reference. The "byte-identical-to-v1.12.0" claim is implicit:
#      the BIC path's only behavioral change is that it is now reached
#      via a dispatch on `lambda_selection = "bic"` instead of being
#      the only option — same internal call to `getBetaBIC()`, same
#      result. Reference values were generated by running v1.13.0's
#      CV/BIC dispatch with `lambda_selection = "bic"` on the fixture
#      below; future refactors that disturb the BIC path will cause
#      this test to fail.
#   6. Invalid `lambda_selection` errors cleanly via the
#      collect-all-violations validator (not `match.arg`).
#   7. `cv_folds < 2` errors cleanly (bounds check).
#   8. `glance()` exposes the three new slots.
#   9. Both paths are exposed for `betwfe()` as well as `fetwfe()`.
#  10. Coverage validation: a small fixed-θ test asserts that
#      `lambda_selection = "cv"` on a representative DGP gives
#      `mean(att_hat - true_att)` bias `< 0.10` in absolute value.
#      This is a coverage-direction check, not a stat-test — the
#      SPEC's empirical validation (Phase B / B-extended) already
#      covered the statistical claim.

library(testthat)
library(fetwfe)

# ------------------------------------------------------------------------------
# Shared fixture: small fixed-θ panel.
# ------------------------------------------------------------------------------

.lambda_sel_fixture <- function(seed = 2026, N = 120) {
	sim_coefs <- genCoefs(
		G = 3,
		T = 5,
		d = 2,
		density = 0.5,
		eff_size = 2,
		seed = seed
	)
	simulateData(
		sim_coefs,
		N = N,
		sig_eps_sq = 1,
		sig_eps_c_sq = 0.5,
		seed = seed
	)
}

# ------------------------------------------------------------------------------
# Test 1: default lambda_selection is "cv".
# ------------------------------------------------------------------------------

test_that("default lambda_selection is 'cv'", {
	sim <- .lambda_sel_fixture()
	res <- fetwfeWithSimulatedData(sim)
	expect_identical(res$lambda_selection, "cv")
	expect_identical(res$cv_folds, 10L)
	# Default cv_seed = as.integer(N * T) = 120 * 5 = 600
	expect_identical(res$cv_seed, 600L)
})

test_that("default lambda_selection is 'cv' for betwfe too", {
	sim <- .lambda_sel_fixture()
	res <- betwfeWithSimulatedData(sim)
	expect_identical(res$lambda_selection, "cv")
	expect_identical(res$cv_folds, 10L)
	expect_identical(res$cv_seed, 600L)
})

# ------------------------------------------------------------------------------
# Test 2: CV path produces finite, sensible results.
# ------------------------------------------------------------------------------

test_that("CV path produces finite att_hat, att_se, lambda_star", {
	sim <- .lambda_sel_fixture()
	res <- fetwfeWithSimulatedData(sim)

	expect_true(is.finite(res$att_hat))
	expect_true(is.finite(res$att_se))
	expect_true(is.finite(res$lambda_star))
	expect_gt(res$att_se, 0)
	expect_gt(res$lambda_star, 0)
	expect_gte(res$lambda_star_model_size, 1L)
})

# ------------------------------------------------------------------------------
# Test 3: CV is deterministic for the same cv_seed.
# ------------------------------------------------------------------------------

test_that("CV path is deterministic for the same cv_seed", {
	sim <- .lambda_sel_fixture()
	res1 <- fetwfeWithSimulatedData(sim, cv_seed = 42L)
	res2 <- fetwfeWithSimulatedData(sim, cv_seed = 42L)
	expect_identical(res1$att_hat, res2$att_hat)
	expect_identical(res1$att_se, res2$att_se)
	expect_identical(res1$lambda_star, res2$lambda_star)
	expect_identical(res1$cv_seed, 42L)
})

test_that("CV path with cv_seed = NULL defaults to as.integer(N * T)", {
	sim <- .lambda_sel_fixture(N = 120)
	res_default <- fetwfeWithSimulatedData(sim)
	expect_identical(res_default$cv_seed, 600L) # 120 * 5

	# Calling explicitly with the same seed should yield byte-identical
	# results.
	res_explicit <- fetwfeWithSimulatedData(sim, cv_seed = 600L)
	expect_identical(res_default$att_hat, res_explicit$att_hat)
})

# ------------------------------------------------------------------------------
# Test 4: CV varies for different cv_seed values.
# ------------------------------------------------------------------------------

test_that("CV path varies for different cv_seed values", {
	sim <- .lambda_sel_fixture()
	# Pre-computed seeds known to produce different CV-selected lambdas
	# on this DGP. (Some seed pairs collide because CV picks the same
	# lambda index when fold assignment doesn't shift the optimum;
	# 100 vs 7 is empirically different on this fixture.)
	res_a <- fetwfeWithSimulatedData(sim, cv_seed = 100L)
	res_b <- fetwfeWithSimulatedData(sim, cv_seed = 7L)
	expect_false(isTRUE(all.equal(res_a$att_hat, res_b$att_hat)))
})

# ------------------------------------------------------------------------------
# Test 5: BIC path produces byte-identical results (regression).
#
# These reference values were generated by running v1.13.0's BIC
# dispatch on the fixture above. The BIC path's only behavioral change
# in v1.13.0 is that it is reached via `lambda_selection = "bic"`
# rather than being the only option; the internal call to
# `getBetaBIC()` is byte-identical to v1.12.0's. Future refactors
# that disturb the BIC path will cause this test to fail.
# ------------------------------------------------------------------------------

test_that("BIC path produces byte-identical results on a small fixed-theta DGP", {
	sim <- .lambda_sel_fixture()
	res <- fetwfeWithSimulatedData(sim, lambda_selection = "bic")

	expect_identical(res$lambda_selection, "bic")
	expect_identical(res$cv_folds, NA_integer_)
	expect_identical(res$cv_seed, NA_integer_)

	# Numerical lock-in — tolerance 1e-12 catches any drift in the BIC
	# math from refactoring while accommodating cross-platform float drift
	# on grpreg+BLAS.
	expect_equal(res$att_hat, 0.4480082058461672, tolerance = 1e-12)
	expect_equal(res$att_se, 0.1356706033541936, tolerance = 1e-12)
	expect_equal(res$lambda_star, 0.01246630388611171, tolerance = 1e-12)
	# Feature count now excludes the always-present intercept (#269); was 18L when
	# the intercept was counted. The locked att/se/lambda_star above are unchanged
	# by that switch (it is a constant offset on the BIC, so selection is identical).
	expect_equal(res$lambda_star_model_size, 17L)
})

test_that("BETWFE BIC path produces byte-identical results", {
	sim <- .lambda_sel_fixture()
	res <- betwfeWithSimulatedData(sim, lambda_selection = "bic")

	expect_identical(res$lambda_selection, "bic")
	# Reference values from v1.13.0 BIC dispatch on the same fixture.
	expect_equal(res$att_hat, 0.4383080388062255, tolerance = 1e-12)
	expect_equal(res$att_se, 0.1329024923053161, tolerance = 1e-12)
	expect_equal(res$lambda_star, 0.02685783754871834, tolerance = 1e-12)
})

